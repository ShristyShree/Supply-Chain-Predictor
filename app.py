def run_ensemble(feature_vector: dict) -> tuple[float, float, float, float]:
    """Returns (xgb_prob, rf_prob, lr_prob, ensemble_prob)."""
    row = pd.DataFrame([feature_vector])[FEATURE_COLS]
    p_xgb = float(xgb.predict_proba(row)[0, 1])
    p_rf  = float(rf.predict_proba(row)[0, 1])
    p_lr  = float(lr.predict_proba(row)[0, 1])
    ens   = round((p_xgb + p_rf + p_lr) / 3, 4)
    return round(p_xgb, 4), round(p_rf, 4), round(p_lr, 4), ens


def run_shap(feature_vector: dict) -> tuple[np.ndarray, list[tuple[str, float]]]:
    """
    Returns raw SHAP values array and sorted [(feature, shap_val)] list.
    Uses TreeExplainer — fast and exact for XGBoost.
    """
    row         = pd.DataFrame([feature_vector])[FEATURE_COLS]
    shap_vals   = shap_explainer.shap_values(row)[0]          # 1-D array
    pairs       = sorted(zip(FEATURE_COLS, shap_vals), key=lambda x: abs(x[1]), reverse=True)
    return shap_vals, pairs


