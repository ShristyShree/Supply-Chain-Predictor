def driver_map(city: str, congestion: float, weather_sev: float) -> go.Figure:
    """
    Simplified route map using Plotly scatter_mapbox.
    Shows origin → destination with intermediate risk zones.
    Replace lat/lon pairs with real route coordinates for production.
    """
    city_centres: dict[str, tuple[float, float]] = {
        "Chennai":   (13.0827, 80.2707),
        "Mumbai":    (19.0760, 72.8777),
        "Delhi":     (28.6139, 77.2090),
        "Bangalore": (12.9716, 77.5946),
        "Kolkata":   (22.5726, 88.3639),
        "Hyderabad": (17.3850, 78.4867),
        "Pune":      (18.5204, 73.8567),
        "Surat":     (21.1702, 72.8311),
    }
    lat0, lon0 = city_centres.get(city, (13.0827, 80.2707))
    # Simulate a route: origin → hub → destination (±0.15°)
    route_lats = [lat0 - 0.15, lat0, lat0 + 0.12, lat0 + 0.20]
    route_lons = [lon0 - 0.10, lon0, lon0 + 0.08, lon0 + 0.18]
    labels     = ["Origin depot", "Checkpoint", "Risk zone", "Destination hub"]

    risk_score = round((congestion + weather_sev) / 2, 2)
    zone_color = "red" if risk_score > 0.6 else "orange" if risk_score > 0.35 else "green"

    fig = go.Figure()

    # Route line
    fig.add_trace(go.Scattermapbox(
        lat=route_lats, lon=route_lons,
        mode="lines",
        line=dict(width=4, color=zone_color),
        name="Route",
        hoverinfo="skip",
    ))

    # Waypoints
    fig.add_trace(go.Scattermapbox(
        lat=route_lats, lon=route_lons,
        mode="markers+text",
        marker=dict(size=[14, 12, 18, 14],
                    color=["#10b981", "#f59e0b", zone_color, "#10b981"]),
        text=labels,
        textposition="top right",
        textfont=dict(size=11, color="#f9fafb"),
        name="Waypoints",
        hovertemplate="%{text}<extra></extra>",
    ))

    fig.update_layout(
        mapbox=dict(style="carto-darkmatter", center=dict(lat=lat0, lon=lon0), zoom=10),
        margin=dict(l=0, r=0, t=0, b=0),
        height=340,
        paper_bgcolor="rgba(0,0,0,0)",
        legend=dict(font=dict(color="#d1d5db")),
    )
    return fig


