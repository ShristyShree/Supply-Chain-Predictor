def shap_bar(shap_pairs: list[tuple[str, float]], role: str, n: int = 8) -> go.Figure:
    """Horizontal bar chart of top SHAP contributions, colour-coded by sign."""
    top    = shap_pairs[:n]
    labels = [p[0].replace("_", " ").title() for p in top]
    vals   = [p[1] for p in top]
    colors = ["#ef4444" if v > 0 else "#3b82f6" for v in vals]

    fig = go.Figure(
        go.Bar(
            x=vals, y=labels,
            orientation="h",
            marker_color=colors,
            text=[f"{v:+.3f}" for v in vals],
            textposition="outside",
            textfont=dict(size=11),
        )
    )
    fig.update_layout(
        title=dict(text=f"Feature contributions — {role} view", font=dict(size=13)),
        xaxis=dict(title="SHAP value", zeroline=True, zerolinecolor="#374151"),
        yaxis=dict(autorange="reversed"),
        height=max(280, n * 34),
        **_DARK_LAYOUT,
    )
    return fig


