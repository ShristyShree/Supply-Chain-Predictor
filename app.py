@st.cache_resource(show_spinner=False)
def load_ensemble():
    """Load pre-trained models from disk. Raises clear error if missing."""
    paths = {
        "xgb": "model/xgb_model.pkl",
        "rf":  "model/rf_model.pkl",
        "lr":  "model/lr_model.pkl",
    }
    missing = [k for k, p in paths.items() if not os.path.exists(p)]
    if missing:
        st.error(
            f"❌ Model files not found: {missing}. "
            "Run `python model/train_model.py` first to generate them."
        )
        st.stop()
    return (
        joblib.load(paths["xgb"]),
        joblib.load(paths["rf"]),
        joblib.load(paths["lr"]),
    )


@st.cache_resource(show_spinner=False)
def load_shap_explainer(_xgb_model):
    """Build TreeExplainer once; reuse across sessions."""
    return shap.TreeExplainer(_xgb_model)


